		$sparql = str_replace('<ISSN>', $issn, $template);
		
		$json = post(
			'https://query.wikidata.org/bigdata/namespace/wdq/sparql', 
			'query=' . $sparql
			);
			
		file_put_contents($filename, $json);
	}
	
	$json = file_get_contents($filename);
	
	$obj = json_decode($json);
	
	if ($obj)
	{
		$record = new stdclass;
		$record->is_in_doaj = 0;
		$record->alternate_titles = [];
		
		foreach ($obj[0] as $k => $v)
		{
			switch ($k)
			{
				case '@id':
					$record->id = str_replace('http://www.wikidata.org/entity/', '', $v);
					break;

				case 'http://example.com/doaj':
					$record->is_in_doaj = 1;
					break;
					
				case 'http://example.com/issn_l':
				case 'http://example.com/jstor':
				case 'http://example.com/jstage':
				case 'http://example.com/dialnet':
				case 'http://example.com/bhl':
				case 'http://example.com/iso4':
				case 'http://example.com/oclc':
				case 'http://example.com/openalex':
				case 'http://example.com/from_year':
				case 'http://example.com/to_year':
				case 'http://example.com/cnki':
				case 'http://example.com/zoobank':
				case 'http://example.com/crossref':
				case 'http://example.com/ndl':
					$record_key = str_replace('http://example.com/', '', $k);
					$record->{$record_key} = $v[0]->{'@value'};
					break;

				case 'http://example.com/homepage_url':
					$record_key = str_replace('http://example.com/', '', $k);
					$record->{$record_key} = $v[0]->{'@id'};
					break;

				case 'http://example.com/rss':
					$record_key = str_replace('http://example.com/', '', $k);
					$record->{$record_key} = $v[0]->{'@id'};
					break;

				case 'http://schema.org/startDate':
				case 'http://schema.org/endDate':
					$record_key = str_replace('http://schema.org/', '', $k);
					$record->{$record_key} = $v[0]->{'@value'};
					break;
					
				case 'http://schema.org/issn':
					$record_key = str_replace('http://schema.org/', '', $k);
					$record->{$record_key} = [];
					
					foreach ($v as $value)
					{
						$record->{$record_key}[] = $value->{'@value'};
					}
					break;
					
				case 'http://example.com/alternate_titles':
					$record_key = str_replace('http://example.com/', '', $k);
					//$record->{$record_key} = [];
					
					foreach ($v as $value)
					{
						$record->{$record_key}[] = $value->{'@value'};
					}
					break;
					

				case 'http://schema.org/alternateName':
					$record_key = str_replace('http://schema.org/', '', $k);
					$record->{$record_key} = [];
					
					foreach ($v as $value)
					{
						$record->{$record_key}[] = $value->{'@value'};
					}
					
					$record->{$record_key} = array_unique($record->{$record_key});
					break;

				case 'http://schema.org/name':
					$record_key = str_replace('http://schema.org/', '', $k);
					$record->display_name = "";
					
					foreach ($v as $value)
					{
						$record->alternate_titles[] = $value->{'@value'};
					
						switch ($value->{'@language'})
