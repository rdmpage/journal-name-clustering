							case 'mul':
								$record->display_name = $value->{'@value'};
								break;
								
							case 'en':
								if ($record->display_name != '')
								{
									$record->display_name = $value->{'@value'};
								}
								break;
								
							default:
								break;
						}
					}
					
					// fallback
					if ($record->display_name == '')
					{
						 $record->display_name = $v[0]->{'@value'};
					}
					
					break;
					
				default:
					break;

			
			}
		
		
		}
		
		if (count($record->alternate_titles) == 0)
		{
			unset($record->alternate_titles);
		}
		else
		{
			$record->alternate_titles = array_unique($record->alternate_titles);
		}
		
		asort($record->issn);
		
		// print_r($record);
		
		$sql = obj_to_sql($record, 'wikidata');
		
